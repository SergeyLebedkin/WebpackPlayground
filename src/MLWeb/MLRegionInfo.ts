// MLRegionInfo
export class MLRegionInfo {
    // fields
    public x: number = 0;
    public y: number = 0;
    public w: number = 0;
    public h: number = 0;

    // constructor
    constructor(x: number, y: number, w: number, h: number) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
    }
}